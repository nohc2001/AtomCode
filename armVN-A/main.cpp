inline bool is1(uint32_t inst, uint32_t loc){
  return inst & (1 << loc);
}

struct op_range{
  int start = 0;
  int end = 0;
};

inline int gr(op_range r, int index){
  return r.end - index;
}

bool masking(uint32_t inst, op_range r, const char* mask){
  int len = 1 + r.end - r.start;
  bool b = true;
  for(int i=0;i<len;++i){
    switch(mask[i]){
      case '1':
      b = b && is1(inst, gr(r, i));
      break;
      case '0':
      b = b && !is1(inst, gr(r, i));
      break;
      case '>':
      return b;
    }
    
    if(b == false) return false;
  }

  return true;
}

struct ccstr{
  char text[124] = {};
  int up = 0;
};

void push_text(ccstr* str, const char* text){
  int len = strlen(text);
  for(int i=0;i<len;++i){
    str->text[str->up] = text[i];
    str->up += 1;
  }
  str->text[str->up] = 0;
}

struct inst_info{
  //mnimonic
  char mnimonic[16] = {};

  //param_array
  int param_len = 0;
  uint32_t params[16] = {};

  // assembly code text
  ccstr asm_text;

  // emulate execute function pointer(uint32* param, int mod)
  // mod - 
  // 0 : emulate / 1 : to machine code / 2 : to assembly
  void (*func)(inst_info*, int);

  //inst type
  op_type inst_type;

  //machine code
  uint32_t inst_data;
};

void Reserved_UDF_func(inst_info* ii, int mod){
  switch(mod){
    case 0:
    break;
    case 1:
    break;
    case 2:
    {
      strcpy(ii->mnimonic, "UDF");
      ii->param_len = 1;
      ii->params[0] = getParam(ii->inst_data, {0, 15});
      push_text(&ii->asm_text, ii->mnimonic);
      push_text(&ii->asm_text, " #");
      push_text(&ii->asm_text, to_string(ii->params[0]).c_str());
    }
    break;
  }
}

uint32_t getParam(uint32_t inst, op_range r){
  uint32_t out = inst;
  int en = (31-r.end);
  out = out << en;
  out = out >> en;
  out = out >> r.start;
  return out;
}

inst_info gii(op_type type, uint32_t inst){
  inst_info ii;
  ii.inst_type = type;
  ii.inst_data = inst;
  switch(ii.inst_type){
    case op_type::Reserved_UDF:
    ii.func = Reserved_UDF_func;
    break;
  }

  ii.func(&ii, 2);

  return ii;
}

